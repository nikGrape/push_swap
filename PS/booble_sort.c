#include "push_swap.h"

void	booble_sort(int *stack)
{
	int i;
	int j;

	i = 1;
	while (i <= LEN)
	{
		j = i;
		while (j <= LEN)
		{
			if (stack[i] < stack[j])
				ft_swapi(&stack[i], &stack[j]);
			j++;
		}
		i++;
	}
}
